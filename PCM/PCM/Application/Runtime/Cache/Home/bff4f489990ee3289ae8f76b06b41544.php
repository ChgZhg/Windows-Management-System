			    <h3> 一．总则 </h3>
			    <p> 全国大学生电工数学建模竞赛（以下简称竞赛）是中国电机工程学会电工数学专委会主办的面向全国大学生的科技活动，目的是提高学生的综合素质、增强创新意  识、培养学生应用数学知识解决实际工程问题的能力，激发学生学习数学的积极性，同时也将推动高校的教学改革与教育创新的进程。 </p>
			    <h3> 二．竞赛内容 </h3>
			    <p> 竞赛题目一般来源于电工、近代数学及经济管理等方面，经过适当的简化、加工的实际问题，主要包括： </p>
			    <p> 1．信息处理问题；</p>
			    <p> 2．控制理论及应用问题；</p>
			    <p> 3．运筹与决策问题；</p>
			    <p> 4．电路与电磁场理论相关问题。</p>
			    <p> 参赛学生应学过普通高校的工科数学课程及相关专业的专业基础知识，不要求参赛者预先掌握深入的专门知识。竞赛题目比较灵活，能够使参赛学生充分发挥其创造  能力。参赛者要根据题目要求，完成一篇包括模型的假设、建立和求解、算法的设计和计算机实现、结果的分析和检验、模型的改进等方面的论文（即答卷）。答卷  的评定以假设的合理性、建模的创造性、结果的可行性和文字的清晰度为主要标准。</p>
			    <h3> 三．竞赛形式、规则和纪律</h3>
			    <p> 1．全国统一竞赛题目，采取网上竞赛方式，以学校为单位进行。</p>
			    <p> 2．竞赛一般在每年5月末的三天内举行。</p>
			    <p> 3．本科生以队为单位参赛，每队3人，专业不限。每队可设一名指导教师（或教师组），对参赛学生进行赛前的辅导及赛前准备工作。竞赛期间指导教师要回避参赛队员，禁止进行指导或参与讨论。</p>
			    <p> 4．竞赛期间参赛学生可以使用各种图书资料、计算机和软件以及在网上浏览，不能与队外任何人讨论。</p>
			    <p> 5．竞赛题目将按照规定时间准时在指定的网站公布，参赛队员在规定的时间内完成答卷，并准时在网上交卷。</p>
			    <p> 6．各参赛院校应责成有关职能部门负责竞赛的组织和纪律监督工作，保证竞赛的规范性和公正性。</p>
			    <h3> 四．组织形式</h3>
			    <p> 竞赛由电工数学专委会主办，负责每年报名、拟定赛题、组织答卷的评定、评奖、印制获奖证书、举办颁奖仪式等。</p>
			    <h3> 五．奖励办法</h3>
			    <p> 1．电工数学专委会聘请专家组成评阅委员会，评选一、二、三等奖，获奖比例一般不超过参赛队数的二分之一，其余凡完成答卷的参赛队获得成功参赛奖。</p>
			    <p> 2．一、二、三等奖均颁发获奖证书。</p>
